fnumber = int(input("Enter your first number :"))
snumber = int(input("Enter your second number :"))
opration = input("Enter any opration like +,-,*,/,%  : ")
if opration == "+":
    print(fnumber + snumber)
elif opration == "-":
    print(fnumber - snumber)
elif opration == "*":
    print(fnumber * snumber)
elif opration == "/":
    print(fnumber / snumber)
elif opration == "%":
    print(fnumber % snumber) 
